    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        add=jTextArea1.getText();
        Connection conn;
        Connect ob=new Connect();
        conn= ob.getConnection();

        String sq1="INSERT INTO user(accounttype,cardnumber,balance,name)VALUES(?,?,?,?)";
        String sq2="INSERT INTO savings(name,cvv,accountno,age,address,phoneno,balance,alternateph,district,state,occupation,gender)VALUES(?,?,?,?,?,?,?,?,?,?,?,?)";
        PreparedStatement ps1,ps2;
        int rs2,rs3;
        try{

            ps1=conn.prepareStatement(sq1);

            ps1.setString(1,ac1);
            ps1.setString(2,jTextField1.getText());
            ps1.setInt(3,Integer.parseInt(jTextField3.getText()));
            ps1.setString(4,name.getText());
            rs2=ps1.executeUpdate();

            ps2=conn.prepareStatement(sq2);
            ps2.setString(1,name.getText());
            ps2.setString(2,jTextField2.getText());
            ps2.setString(3,jTextField1.getText());
            ps2.setString(4,age.getText());
            ps2.setString(5,jTextArea1.getText());
            ps2.setString(6,phoneno.getText());
            ps2.setString(7,jTextField3.getText());
            ps2.setString(8,altph.getText());
            ps2.setString(9,district.getText());
            ps2.setString(10,state.getText());
            ps2.setString(11,occupation.getText());
            ps2.setString(12,jComboBox1.getSelectedItem().toString());
            rs3=ps2.executeUpdate();

            if(rs3>0)
            { JOptionPane.showMessageDialog(null,"Savings  account saved");}
        }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,ex);}

    }//GEN-LAST:event_jButton1ActionPerformed

